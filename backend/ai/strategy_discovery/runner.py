"""Discovery runner — orchestrates a full search batch end-to-end.

A run takes a (kind, mode, config) tuple, samples or evolves N candidates
from the search space, evaluates each against the universe, optionally
computes walk-forward folds, persists results to Supabase, and updates
the parent run row with aggregates.

Two search modes:
  * `random` — N random draws from the search space (fast, broad)
  * `ga`     — multi-generation elitist GA (slower, focused)

Walk-forward: when `walk_forward_folds > 1`, every candidate that
qualifies (enough bars, >0 trades on the full window) gets a per-fold
breakdown persisted into `walk_forward` JSONB. A stability penalty
based on fold-to-fold Sharpe variance is folded into the composite
score so a strategy that "decays from 2.0 → -0.5" gets ranked below
one with a flat 0.9 across all folds.

Concurrency: candidates are evaluated sequentially per-symbol but
multiple symbols are processed in a thread pool (with per-symbol bars
cached up front).

LOCKED: candidates are generated by deterministic search over the DSL
parameter space — never by an LLM. See
``project_agents_decision_2026_05_10`` memory entry.
"""

