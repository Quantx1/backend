@router.post("/candidates/{candidate_id}/promote")
async def promote_candidate(
    candidate_id: str,
    payload: PromoteRequest,
    user=Depends(get_current_user),
) -> Dict[str, Any]:
    """Copy the candidate's DSL into the user's strategy library.

    Creates a row in `user_strategies` referencing the discovered strategy.
    Validates the DSL through Strategy.model_validate first so we never
    write a malformed document.
    """
    sb = get_supabase_admin()

    # Load the candidate
    res = sb.table("discovered_strategies").select("*").eq("id", candidate_id).limit(1).execute()
    if not res.data:
        raise HTTPException(404, "candidate not found")
    cand = res.data[0]
    if cand["status"] != "candidate":
        raise HTTPException(
            400, f"candidate is already {cand['status']} — only `candidate` rows can be promoted",
        )

    # Validate DSL
    try:
        strategy = Strategy.model_validate(cand["dsl"])
    except ValidationError as e:
        raise HTTPException(400, f"candidate DSL failed validation: {e}")

    # Optional name override
    name = payload.name or cand.get("label") or strategy.name
    if payload.mode not in ("paper", "live"):
        raise HTTPException(400, "mode must be paper|live")

    # Insert into user_strategies
    new_strategy_id = str(uuid4())
    dsl_doc = strategy.model_dump(mode="json")
    dsl_doc["name"] = name
    dsl_doc["mode"] = payload.mode
    user_strategy_row = {
        "id": new_strategy_id,
        "user_id": user.id,
        "name": name[:120],
        "dsl": dsl_doc,
        "mode": payload.mode,
        "source": "discovery",
        "source_ref": candidate_id,
    }
    sb.table("user_strategies").insert(user_strategy_row).execute()

    # Mark the candidate as promoted
    sb.table("discovered_strategies").update({
        "status": "promoted",
        "promoted_to_strategy_id": new_strategy_id,
        "promoted_at": datetime.now(timezone.utc).isoformat(),
        "promoted_by_user_id": user.id,
    }).eq("id", candidate_id).execute()

    return {
        "promoted": True,
        "candidate_id": candidate_id,
        "user_strategy_id": new_strategy_id,
        "mode": payload.mode,
    }


@router.post("/candidates/{candidate_id}/archive")
async def archive_candidate(
    candidate_id: str,
    user=Depends(get_current_user),
) -> Dict[str, Any]:
    """Soft-archive a candidate — hides it from the leaderboard but keeps
    the row for audit / re-promote later."""
    sb = get_supabase_admin()
    res = sb.table("discovered_strategies").update({
        "status": "archived",
    }).eq("id", candidate_id).execute()
    if not res.data:
        raise HTTPException(404, "candidate not found")
    return {"archived": True, "candidate_id": candidate_id}
