"""Strategy Discovery REST surface (PR-G2).

Endpoints:
  POST   /api/discovery/runs                — kick off a new discovery batch
  GET    /api/discovery/runs                — list past runs (most recent first)
  GET    /api/discovery/runs/{id}           — run detail + status
  GET    /api/discovery/runs/{id}/candidates— top candidates from a run
  GET    /api/discovery/candidates/{id}     — single candidate detail
  POST   /api/discovery/candidates/{id}/promote — promote to user_strategies
  POST   /api/discovery/candidates/{id}/archive — dismiss

The actual discovery work is offloaded to a background thread so the
POST returns immediately with the run id; clients poll via
``GET /runs/{id}`` for status. For a 30-candidate × 8-symbol equity_swing
run that's ~45-90 seconds of CPU; the runner persists progress so the
client always has *some* data to show even mid-run.

LOCKED: every candidate stored or promoted was generated by deterministic
search — never by an LLM. See project_agents_decision_2026_05_10.
"""

from __future__ import annotations

import logging
import threading
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional
from uuid import UUID, uuid4

from fastapi import APIRouter, Depends, HTTPException, Query
from pydantic import BaseModel, Field, ValidationError

from ..ai.strategy.dsl import Strategy
from ..ai.strategy_discovery import DiscoveryConfig, run_discovery
from ..core.database import get_supabase_admin
from ..core.security import get_current_user

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/api/discovery", tags=["Strategy Discovery"])


# Bounds on user-supplied input — tight enough that one user can't tie
# up the whole worker thread for hours.
MIN_SAMPLE_SIZE = 5
MAX_SAMPLE_SIZE = 60
MIN_SYMBOLS = 1
MAX_SYMBOLS = 20


# ─────────────────────────────────────────────────────────────────────
# Request / response shapes
# ─────────────────────────────────────────────────────────────────────


class CreateRunRequest(BaseModel):
    """User-facing payload to start a discovery run.

    Mirrors `DiscoveryConfig` but with stricter bounds so the worker
    can't be DoSed by a 10,000-sample request.
    """
    kind: str = Field(
        description=("equity_swing | equity_position | fo_weekly | fo_monthly"),
    )
    mode: str = Field(
        default="random",
        description=("`random` = sample_size random draws, fast/broad. "
                     "`ga` = elitist GA, pop_size × generations evaluations, "
                     "focused on near-best neighborhoods."),
    )
    sample_size: int = Field(default=20, ge=MIN_SAMPLE_SIZE, le=MAX_SAMPLE_SIZE)
    universe: str = Field(
        default="nifty50",
        description=("For equity: nifty50/100/500/250. For F&O: NIFTY|BANKNIFTY|FINNIFTY."),
    )
    symbols_per_candidate: int = Field(default=6, ge=MIN_SYMBOLS, le=MAX_SYMBOLS)
    history_period: str = Field(default="2y")
    seed: int = Field(default=42)

    # GA-mode knobs (ignored when mode='random')
    ga_pop_size: int = Field(default=12, ge=4, le=30)
    ga_generations: int = Field(default=3, ge=1, le=6)
    ga_elite: int = Field(default=4, ge=1, le=10)
    ga_children_per_elite: int = Field(default=2, ge=1, le=5)

    # Walk-forward — 0/1 disables. 3 = standard 3-fold (needs ~240 bars).
    walk_forward_folds: int = Field(default=0, ge=0, le=5)


class RunSummary(BaseModel):
    id: str
    kind: str
    status: str
    started_at: Optional[str]
    completed_at: Optional[str]
    candidates_total: int
    candidates_viable: int
    best_score: Optional[float]
    triggered_by_user_id: Optional[str]


class CandidateSummary(BaseModel):
    id: str
    run_id: str
    kind: str
    label: str
    score: float
    sharpe: Optional[float]
    calmar: Optional[float]
    max_drawdown_pct: Optional[float]
    win_rate: Optional[float]
    profit_factor: Optional[float]
    total_return_pct: Optional[float]
    trade_count: Optional[int]
    avg_hold_days: Optional[float]
    regime_scores: Dict[str, Any]
    status: str
    created_at: Optional[str]


class CandidateDetail(CandidateSummary):
    """Full candidate with DSL document — used on the promote review page."""
    dsl: Dict[str, Any]
    walk_forward: List[Dict[str, Any]]
    promoted_to_strategy_id: Optional[str]
    promoted_at: Optional[str]


class PromoteRequest(BaseModel):
    name: Optional[str] = Field(
        default=None, max_length=120,
        description=("Override the strategy name. Defaults to the candidate's name."),
    )
    mode: str = Field(
        default="paper",
        description=("paper | live — initial mode when copied to user_strategies"),
    )


# ─────────────────────────────────────────────────────────────────────
# Background worker — runs the discovery in a daemon thread.
# ─────────────────────────────────────────────────────────────────────


def _kick_off(cfg: DiscoveryConfig, user_id: Optional[UUID]) -> None:
    """Spawn the runner without blocking the request.

    The runner already handles all status persistence and error capture,
    so the caller doesn't need a return path — failures show up as
    `status='failed'` rows that the UI surfaces.
    """
    def _target():
        try:
            run_discovery(cfg, triggered_by_user_id=user_id)
        except Exception as e:
            logger.exception("background discovery failed: %s", e)

    t = threading.Thread(target=_target, name=f"discovery-{cfg.kind}", daemon=True)
    t.start()


