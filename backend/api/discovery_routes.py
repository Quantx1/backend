@router.post("/runs", status_code=201)
async def create_run(
    payload: CreateRunRequest,
    user=Depends(get_current_user),
) -> Dict[str, Any]:
    """Kick off a new discovery run. Returns immediately with the run id."""
    if payload.kind not in (
        "equity_swing", "equity_position",
        "fo_weekly", "fo_monthly",
        "intraday_5m", "intraday_15m",
    ):
        raise HTTPException(400, f"invalid kind: {payload.kind}")
    # F&O kinds require an index underlying, not a sector tier
    if payload.kind.startswith("fo_") and payload.universe.upper() not in (
        "NIFTY", "BANKNIFTY", "FINNIFTY",
    ):
        raise HTTPException(
            400, f"F&O discovery requires universe in NIFTY|BANKNIFTY|FINNIFTY, got {payload.universe}",
        )
    # Intraday: keep universe small (data heavy). Default to nifty50 if
    # caller picks an unsupported tier.
    if payload.kind.startswith("intraday_") and payload.universe not in (
        "nifty50", "nifty100",
    ):
        payload.universe = "nifty50"

    cfg = DiscoveryConfig(
        kind=payload.kind,
        mode=payload.mode,
        sample_size=payload.sample_size,
        universe=payload.universe,
        symbols_per_candidate=payload.symbols_per_candidate,
        history_period=payload.history_period,
        seed=payload.seed,
        ga_pop_size=payload.ga_pop_size,
        ga_generations=payload.ga_generations,
        ga_elite=payload.ga_elite,
        ga_children_per_elite=payload.ga_children_per_elite,
        walk_forward_folds=payload.walk_forward_folds,
    )

    # Insert a placeholder row immediately so the user gets a stable id
    # back to poll. The worker then runs `run_discovery(existing_run_id=…)`
    # which updates THIS row in place — no duplicate rows.
    sb = get_supabase_admin()
    placeholder = sb.table("strategy_search_runs").insert({
        "kind": payload.kind,
        "config": cfg.to_dict(),
        "status": "pending",
        "triggered_by_user_id": user.id if user else None,
    }).execute()
    placeholder_id = placeholder.data[0]["id"]

    def _target():
        try:
            from backend.ai.strategy_discovery import run_discovery
            run_discovery(cfg, existing_run_id=placeholder_id)
        except Exception as e:
            logger.exception("discovery worker failed: %s", e)
            # run_discovery already marks the row failed on exception

    threading.Thread(target=_target, name=f"discovery-{cfg.kind}", daemon=True).start()

    return {
        "run_id": placeholder_id,
        "status": "pending",
        "kind": payload.kind,
        "config": cfg.to_dict(),
    }


@router.get("/runs")
async def list_runs(
    kind: Optional[str] = Query(None),
    status: Optional[str] = Query(None),
    limit: int = Query(30, ge=1, le=100),
    user=Depends(get_current_user),
) -> Dict[str, List[Dict[str, Any]]]:
    """List recent runs — for the Discovered tab landing page."""
    sb = get_supabase_admin()
    q = sb.table("strategy_search_runs").select(
        "id,kind,status,started_at,completed_at,candidates_total,"
        "candidates_viable,best_score,triggered_by_user_id,error,created_at"
    )
    if kind:
        q = q.eq("kind", kind)
    if status:
        q = q.eq("status", status)
    q = q.order("created_at", desc=True).limit(limit)
    res = q.execute()
    return {"runs": res.data or []}


@router.get("/runs/{run_id}")
async def get_run(run_id: str, user=Depends(get_current_user)) -> Dict[str, Any]:
    sb = get_supabase_admin()
    res = sb.table("strategy_search_runs").select("*").eq("id", run_id).limit(1).execute()
    if not res.data:
        raise HTTPException(404, "run not found")
    return res.data[0]


@router.get("/runs/{run_id}/candidates")
async def list_candidates(
    run_id: str,
    limit: int = Query(20, ge=1, le=100),
    only_viable: bool = Query(True),
    user=Depends(get_current_user),
) -> Dict[str, List[Dict[str, Any]]]:
    """Top-K candidates for a run, sorted by score DESC."""
    sb = get_supabase_admin()
    q = sb.table("discovered_strategies").select(
        "id,run_id,kind,label,score,sharpe,calmar,max_drawdown_pct,"
        "win_rate,profit_factor,total_return_pct,trade_count,avg_hold_days,"
        "regime_scores,status,created_at"
    ).eq("run_id", run_id)
    if only_viable:
        q = q.eq("status", "candidate")
    q = q.order("score", desc=True).limit(limit)
    res = q.execute()
    return {"candidates": res.data or []}


